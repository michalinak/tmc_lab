package pg.eti.tmc.jeppensen.lab.server;

/**
 *
 * @author piotr
 */
public class Position {
    private double x;
    private double y;

    public Position(){}

    public Position(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
    
    
    
}
